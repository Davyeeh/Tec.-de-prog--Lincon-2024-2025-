def celsius_para_fahrenheit(celcius):
    fahrenheit = (celcius * 9/5) + 32
    return fahrenheit

def fahrenheit_para_celsius(fahrenheit):
    celsius = (fahrenheit - 32) * 5/9
    return celsius

def metros_para_pes(metros):
    pes = metros * 3.28084
    return pes

def pes_para_metros(pes):
    metros = pes / 3.28084
    return metros

def quilometros_para_milhas(quilometros):
    milhas = quilometros * 0.621371
    return milhas

def milhas_para_quilometros(milhas):
    quilometros = milhas / 0.621371
    return quilometros

def dia_para_horas(dias):
    horas = dias * 24
    return horas
